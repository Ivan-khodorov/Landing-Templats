export const REGEX = {
  // простой и практичный email-паттерн (без «RFC-джунглей»)
  email: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,

  // либерально: допускаем +, пробелы, дефисы и скобки; >=7 символов
  phone: /^\+?[0-9\s\-()]{7,}$/,

  // http/https + домены с поддоменами; путь — без пробелов
  url: /^(https?:\/\/)?([\w-]+\.)+[\w-]{2,}(\/[^\s]*)?$/i,
};
